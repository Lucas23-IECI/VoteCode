function isConfigured() {
  return (
    Boolean(config.supabaseUrl) &&
    Boolean(config.supabaseAnonKey) &&
    config.supabaseAnonKey !== "PEGA_AQUI_TU_SUPABASE_ANON_KEY"
  );
}

function formatPrice(value) {
  return `$${pesoFormatter.format(value)}`;
}

function escapeHtml(value) {
  return String(value).replace(/[&<>"']/g, (char) => {
    const entities = {
      "&": "&amp;",
      "<": "&lt;",
      ">": "&gt;",
      '"': "&quot;",
      "'": "&#039;",
    };

    return entities[char];
  });
}

function getUserDisplay(user) {
  const meta = user?.user_metadata || {};

  return {
    id: user.id,
    name: meta.full_name || meta.name || user.email || "Jugador",
    email: user.email || "",
    avatarUrl: meta.avatar_url || "",
  };
}

let googleInitialized = false;

function initGoogleAuth() {
  if (googleInitialized) return true;
  if (config.googleClientId && window.google) {
    window.google.accounts.id.initialize({
      client_id: config.googleClientId,
      callback: handleGoogleSignIn,
    });
    googleInitialized = true;
    return true;
  }
  return false;
}

window.onGoogleLibraryLoad = function() {
  if (initGoogleAuth()) {
    if (!state.user) {
      renderAuth();
    }
  }
};

function checkAndClearUrlErrors() {
  const urlParams = new URLSearchParams(window.location.search);
  if (urlParams.has("error") || urlParams.has("error_description")) {
    const errorMsg = urlParams.get("error_description") || urlParams.get("error") || "Error de autenticación";
    console.warn("OAuth error detected in URL:", errorMsg);
    
    // We set the status message later after DOM renders, but let's schedule it
    setTimeout(() => {
      if (els.statusMessage) {
        els.statusMessage.textContent = `Error: ${errorMsg}. Intenta iniciar sesión otra vez.`;
      }
    }, 100);
    
    // Clean URL query parameters without reloading
    const cleanUrl = window.location.origin + window.location.pathname;
    window.history.replaceState({}, document.title, cleanUrl);
  }
}

async function initialize() {
  state.configured = isConfigured();

  if (!state.configured) {
    state.results = buildResults([]);
    render();
    els.statusMessage.textContent = "Falta configurar la anon key de Supabase en frontend/config.js.";
    return;
  }

  // Limpiar errores del URL antes de inicializar para evitar bucles de errores
  checkAndClearUrlErrors();

  supabaseClient = window.supabase.createClient(config.supabaseUrl, config.supabaseAnonKey);
  
  initGoogleAuth();

  supabaseClient.auth.onAuthStateChange(async () => {
    await loadState();
  });
}

let isStateLoading = false;

async function loadState() {
  if (isStateLoading) return;
  isStateLoading = true;

  try {
    const { data: sessionData, error: sessionError } = await supabaseClient.auth.getSession();
    if (sessionError) throw sessionError;

    const user = sessionData.session?.user || null;
    state.user = user ? getUserDisplay(user) : null;

    if (user) {
      try {
        await upsertProfile(user);
      } catch (profileError) {
        console.error("Error updating profile:", profileError);
      }
    }

    const [votes, myVotes] = await Promise.all([
      fetchVotes(),
      user ? fetchMyVotes(user.id) : []
    ]);
    state.results = buildResults(votes);
    state.myVotes = new Set(myVotes);
    state.recentBallots = buildRecentBallots(votes);

    // Si no hay un mensaje de error previo ya mostrado en statusMessage, lo limpiamos
    if (els.statusMessage && !els.statusMessage.textContent.startsWith("Error:")) {
      els.statusMessage.textContent = "";
    }
    
    render();
  } catch (error) {
    console.error("Error loading application state:", error);
    if (els.statusMessage && !els.statusMessage.textContent.startsWith("Error:")) {
      els.statusMessage.textContent = "Error al cargar datos. Intenta recargar la página.";
    }
    // Renderizamos al menos los juegos estáticos para que la página no quede vacía
    state.results = buildResults([]);
    render();
  } finally {
    isStateLoading = false;
  }
}

async function upsertProfile(user) {
  const profile = getUserDisplay(user);
  const { error } = await supabaseClient.from("profiles").upsert({
    user_id: user.id,
    display_name: profile.name,
    email: profile.email,
    avatar_url: profile.avatarUrl,
    updated_at: new Date().toISOString(),
  });

  if (error) throw error;
}

async function fetchVotes() {
  const { data, error } = await supabaseClient
    .from("votes")
    .select("user_id, game_id, created_at, profiles(display_name, avatar_url)")
    .order("created_at", { ascending: false });

  if (error) throw error;
  return data || [];
}

async function fetchMyVotes(userId) {
  const { data, error } = await supabaseClient
    .from("votes")
    .select("game_id")
    .eq("user_id", userId)
    .order("game_id");

  if (error) throw error;
  return (data || []).map((vote) => vote.game_id);
}

function buildResults(votes) {
  const counts = new Map();
  const userIds = new Set();

  for (const vote of votes) {
    userIds.add(vote.user_id);
    counts.set(vote.game_id, (counts.get(vote.game_id) || 0) + 1);
  }

  state.totalBallots = userIds.size;
  state.totalSelections = votes.length;

  return games
    .map((game, index) => {
      const voteCount = counts.get(game.id) || 0;
      const percentage = state.totalBallots ? Math.round((voteCount / state.totalBallots) * 100) : 0;

      return {
        ...game,
        index,
        votes: voteCount,
        percentage,
      };
    })
    .sort((a, b) => b.votes - a.votes || a.index - b.index);
}

function buildRecentBallots(votes) {
  const grouped = new Map();

  for (const vote of votes) {
    const profile = vote.profiles || {};
    const current = grouped.get(vote.user_id) || {
      userId: vote.user_id,
      displayName: profile.display_name || "Jugador",
      avatarUrl: profile.avatar_url || "",
      votedAt: vote.created_at,
      picks: 0,
    };

    current.picks += 1;
    if (vote.created_at > current.votedAt) current.votedAt = vote.created_at;
    grouped.set(vote.user_id, current);
  }

  return [...grouped.values()]
    .sort((a, b) => b.votedAt.localeCompare(a.votedAt))
    .slice(0, 8);
}

function sortedResults() {
  return [...state.results].sort((a, b) => b.votes - a.votes || a.index - b.index);
}

function selectionIsValid() {
  const count = state.myVotes.size;
  return state.user && count >= minVotes && count <= maxVotes;
}

